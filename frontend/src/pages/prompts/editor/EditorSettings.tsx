import { AlertCircle } from 'lucide-react'
import { Link } from 'react-router-dom'

import { Alert, AlertDescription } from '@/components/ui/alert'
import { Badge } from '@/components/ui/badge'
import { Card, CardContent, CardHeader, CardTitle } from '@/components/ui/card'
import { Input } from '@/components/ui/input'
import { Label } from '@/components/ui/label'
import {
  Select,
  SelectContent,
  SelectItem,
  SelectTrigger,
  SelectValue,
} from '@/components/ui/select'
import { Switch } from '@/components/ui/switch'
import { Textarea } from '@/components/ui/textarea'

import type { EditorSettingsProps } from './types'

export function EditorSettings({
  formData,
  errors,
  projects,
  loadingProjects,
  isCheckingSlug,
  slugAutoGenerated,
  onSlugChange,
  onFormDataChange,
}: EditorSettingsProps) {
  return (
    <Card>
      <CardHeader>
        <CardTitle className="text-base">Settings</CardTitle>
      </CardHeader>
      <CardContent className="space-y-4">
        <div className="space-y-1.5">
          <Label htmlFor="prompt-slug">
            Slug
            <span className="text-muted-foreground ml-1 text-xs font-normal">
              (URL-friendly)
            </span>
          </Label>
          <Input
            id="prompt-slug"
            type="text"
            data-testid="prompt-slug-input"
            value={formData.slug}
            onChange={e => {
              onSlugChange(e.target.value)
            }}
            placeholder="auto-generated"
            className="font-mono"
            aria-invalid={!!errors.slug}
          />
          {isCheckingSlug && (
            <p className="text-muted-foreground text-xs">Checking…</p>
          )}
          {errors.slug && (
            <p className="text-destructive flex items-center gap-1 text-xs">
              <AlertCircle className="size-3" />
              {errors.slug}
            </p>
          )}
          {!errors.slug && !slugAutoGenerated && (
            <p className="text-muted-foreground text-xs">
              Custom slug (won&apos;t auto-update)
            </p>
          )}
        </div>

        <div className="space-y-1.5">
          <Label htmlFor="prompt-project">
            Project <span className="text-destructive">*</span>
          </Label>
          {loadingProjects ? (
            <p className="text-muted-foreground text-sm">Loading projects…</p>
          ) : projects.length === 0 ? (
            <Alert>
              <AlertDescription className="text-sm">
                No projects available. Please{' '}
                <Link to="/settings/projects/create" className="underline">
                  create a project
                </Link>{' '}
                first.
              </AlertDescription>
            </Alert>
          ) : (
            <Select
              value={formData.project_id}
              onValueChange={v => {
                onFormDataChange(prev => ({ ...prev, project_id: v }))
              }}
            >
              <SelectTrigger
                id="prompt-project"
                aria-invalid={!!errors.project_id}
              >
                <SelectValue placeholder="Select a project…" />
              </SelectTrigger>
              <SelectContent>
                {projects.map(project => (
                  <SelectItem key={project.id} value={project.id}>
                    {project.name}
                  </SelectItem>
                ))}
              </SelectContent>
            </Select>
          )}
          {errors.project_id && (
            <p className="text-destructive flex items-center gap-1 text-xs">
              <AlertCircle className="size-3" />
              {errors.project_id}
            </p>
          )}
        </div>

        <div className="space-y-1.5">
          <Label htmlFor="prompt-description">
            Description
            <span className="text-muted-foreground ml-1 text-xs font-normal">
              (max 200)
            </span>
          </Label>
          <Textarea
            id="prompt-description"
            value={formData.description}
            onChange={e => {
              onFormDataChange(prev => ({
                ...prev,
                description: e.target.value,
              }))
            }}
            placeholder="Enter a brief description…"
            rows={3}
            maxLength={200}
            aria-invalid={!!errors.description}
          />
          <div className="flex items-center justify-between">
            {errors.description ? (
              <p className="text-destructive flex items-center gap-1 text-xs">
                <AlertCircle className="size-3" />
                {errors.description}
              </p>
            ) : (
              <span />
            )}
            <p className="text-muted-foreground text-xs">
              {formData.description.length}/200
            </p>
          </div>
        </div>

        <div className="space-y-1.5">
          <Label htmlFor="prompt-labels">
            Labels
            <span className="text-muted-foreground ml-1 text-xs font-normal">
              (max 10)
            </span>
          </Label>
          <div className="flex flex-wrap gap-1.5">
            {formData.labels.map((label, index) => (
              <Badge key={index} variant="secondary" className="gap-1">
                {label}
                <button
                  type="button"
                  onClick={() => {
                    onFormDataChange(prev => ({
                      ...prev,
                      labels: prev.labels.filter((_, i) => i !== index),
                    }))
                  }}
                  className="hover:text-foreground"
                  aria-label={`Remove ${label}`}
                >
                  ×
                </button>
              </Badge>
            ))}
          </div>
          <Input
            id="prompt-labels"
            type="text"
            placeholder="Type and press Enter…"
            maxLength={50}
            onKeyDown={e => {
              if (e.key !== 'Enter') return
              e.preventDefault()
              const input = e.currentTarget
              const value = input.value.trim()
              if (
                value &&
                !formData.labels.includes(value) &&
                formData.labels.length < 10
              ) {
                onFormDataChange(prev => ({
                  ...prev,
                  labels: [...prev.labels, value],
                }))
                input.value = ''
              }
            }}
          />
          <p className="text-muted-foreground text-xs">
            {formData.labels.length}/10 labels
          </p>
        </div>

        <div className="space-y-1.5">
          <Label htmlFor="prompt-status">Status</Label>
          <Select
            value={formData.status}
            onValueChange={v => {
              const newStatus = v as 'draft' | 'published'
              onFormDataChange(prev => ({
                ...prev,
                status: newStatus,
                mcp_expose: newStatus === 'draft' ? false : prev.mcp_expose,
              }))
            }}
          >
            <SelectTrigger id="prompt-status">
              <SelectValue />
            </SelectTrigger>
            <SelectContent>
              <SelectItem value="draft">Draft</SelectItem>
              <SelectItem value="published">Published</SelectItem>
            </SelectContent>
          </Select>
        </div>

        {formData.status === 'published' && (
          <div className="flex items-center justify-between">
            <div>
              <Label
                htmlFor="prompt-mcp-expose"
                className="text-sm font-medium"
              >
                Available in MCP
              </Label>
              <p className="text-muted-foreground text-xs">
                Allow AI assistants to discover this prompt via MCP.
              </p>
            </div>
            <Switch
              id="prompt-mcp-expose"
              checked={formData.mcp_expose}
              onCheckedChange={checked => {
                onFormDataChange(prev => ({ ...prev, mcp_expose: checked }))
              }}
            />
          </div>
        )}
      </CardContent>
    </Card>
  )
}
